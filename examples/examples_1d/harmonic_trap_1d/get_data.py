import numpy as np


def get_data(solver):

    # ---------------------------------------------------------------------------------------------
    N = solver.compute_n_atoms('psi')
    # ---------------------------------------------------------------------------------------------

    # ---------------------------------------------------------------------------------------------
    V = solver.get('V')
    # ---------------------------------------------------------------------------------------------

    # ---------------------------------------------------------------------------------------------
    psi = solver.get('psi')

    density = np.abs(psi) ** 2
    # ---------------------------------------------------------------------------------------------

    # ---------------------------------------------------------------------------------------------
    data = type('', (), {})()

    data.N = N

    data.V = V

    data.psi = psi

    data.density = density
    # ---------------------------------------------------------------------------------------------

    return data
